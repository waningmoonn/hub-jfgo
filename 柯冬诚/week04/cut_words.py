# 待切分文本
sentence = "经常有意见分歧"


# 实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(s, word_dict):
    n = len(s)
    result = []
    stack = [(0, [])]  # (当前位置, 当前切分结果)

    while stack:
        pos, current = stack.pop()

        if pos == n:
            result.append(current)
            continue

        # 尝试所有可能的切分，从长词到短词
        for j in range(n, pos, -1):
            word = s[pos:j]
            if word in word_dict:
                stack.append((j, current + [word]))

    return sorted(result, key=lambda x: (len(x), x))


