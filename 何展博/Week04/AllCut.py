#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    results = []

    def backtrack(start, path):
        # 当到达句子末尾时，将当前切分加入结果
        if start == len(sentence):
            results.append(path[:])
            return

        # 尝试所有可能的切分长度（从1到剩余长度）
        for end in range(start + 1, len(sentence) + 1):
            word = sentence[start:end]
            # 如果当前子串在词典中，继续递归
            if word in Dict:
                path.append(word)
                backtrack(end, path)
                path.pop()  # 回溯

    backtrack(0, [])
    return results

list =all_cut(sentence,Dict)
for item in list:
    print(item)


