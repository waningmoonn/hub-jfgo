#待切分文本
sentence = "经常有意见分歧"

#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    n = len(sentence)
    # dp[i]存储所有能切分到位置i的路径
    dp = [[] for _ in range(n + 1)]
    dp[0] = [[]]  # 起始位置，空路径
    # 动态规划填充dp数组
    for i in range(n + 1):
        if not dp[i]:  # 如果当前位置没有可达路径，跳过
            continue
        for j in range(i + 1, n + 1):
            word = sentence[i:j]
            if word in Dict:
                # 将当前词添加到所有到达位置i的路径中
                for path in dp[i]:
                    new_path = path + [word]
                    dp[j].append(new_path)
    return dp[n]

