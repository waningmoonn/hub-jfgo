#待切分文本
sentence = "经常有意见分歧"



#实现全切分函数，输出根据字典能够切分出的所有的切分方式
def all_cut(sentence, Dict):
    #TODO

    n = len(sentence)
    # 动态规划，dp[i]存储从位置i开始的所有切分方式
    dp = [[] for _ in range(n + 1)]
    dp[n] = [[]]  # 句子末尾，有一个空切分

    # 从后往前计算
    for i in range(n - 1, -1, -1):
        # 尝试所有可能的结束位置
        for j in range(i + 1, n + 1):
            word = sentence[i:j]
            # 如果词在词典中
            if word in Dict:
                # 将当前词与dp[j]中的所有切分组合
                for seg in dp[j]:
                    dp[i].append([word] + seg)

    target = dp[0]
    return target

# dp[7] = [[]]
